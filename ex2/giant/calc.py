lst = [0x41, 0x49, 0x43, 0x49, 0x44, 0x4c, 0x4d, 0x47, 0x49, 0x49, 0x41, 0x4f, 0x45, 0x4e, 0x47, 0x4c, 0x41, 0x48, 0x45]

results = []
for i in range(18, -1, -1):
	x = lst[i] - 0x41
	y = (lst[0] if i == 18 else results[0]) - 0x41  # results[0] is always the last one we calculated

	res = (x ^ y) + 0x41
	results.insert(0, res)

results = [hex(ele) for ele in results]
print(results)


